class Employee {

	String EmpId;
	String Name;
	String Salary;

	public String getEmpId(){
	return 	this.EmpId;
	}
	public void setEmpId(String Id){
	this.EmpId = Id;
	}
	public String getName(){
	return this.Name;
	}
	public void setName(String Name){
	this.Name = Name;
	}
	public String getSalary(){
	return this.Salary;
	}
	public void setSalary(String Salary){
	this.Salary = Salary;
	}
}